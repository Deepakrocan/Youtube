# Bitwise Operators
a = 5  # 0101 in binary
b = 3  # 0011 in binary
print(a & b)  # 1 (0001 in binary)
print(a | b)  # 7 (0111 in binary)
print(a ^ b)  # 6 (0110 in binary)
print(~a)  # -6 (two's complement)
print(a << 2)  # 20 (10100 in binary)
print(a >> 2)  # 1 (0001 in binary)\


